# Tipos int e float
# int -> número inteiro
# o tipo int representa qualquer número
# positivo ou negativo, sem sinal é positivo
print(11)
print(-11)
print(0)


# float -> Número com ponto flutuante
# o tipo float representa qualquer número 
# positivo ou negativo com ponto flutuante
# sem sinal é positivo
print(1., 10.11)
print(-1.1)
print(0.0   )



#função Type mostra o tipo que o Python inferiu ao valor
print(type('aaaaaa'))
print(type(0.0))
print(type(1))


