# Tipos de dados

"""Python = Linguagem de Programação
Tipo de tipagem = Dinâmica / Forte
str --> string -> texto
Strings são textos, escritos dentro de aspas"""

# Aspas simples
print('Um texto')
print(1, 'Um "texto"')

# Aspas duplas
print("Um texto")
print(2, "Um 'texto'")
# Escape
print("Um \"texto\"") 
# r -> mostra o caracter de escape
print(r"Um \"texto\"")
